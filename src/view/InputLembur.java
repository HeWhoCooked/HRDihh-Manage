    private void isiComboKaryawan() {
        LembuNamaCBox.removeAllItems();
        List<Pegawai> karyawanList = KaryawanController.daftarKaryawan();
        for (Pegawai p : karyawanList) {
            LembuNamaCBox.addItem(p.getNama());
        }
    }

    private void isiComboDepartemen() {
        LembuDeptCBox2.removeAllItems();
        List<Departemen> deptList = KaryawanController.daftarDepartemen();
        for (Departemen d : deptList) {
            LembuDeptCBox2.addItem(d.getNama());
        }
    }

    private void isiComboLamaLembur() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        for (int i = 1; i <= 24; i++) {
            model.addElement(i + " jam");
        }
        LembuLamaCBox.setModel(model);
    }

