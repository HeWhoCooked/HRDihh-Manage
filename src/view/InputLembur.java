    private void SimpanLembuBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanLembuBTActionPerformed
        String nama = (String) LembuNamaCBox.getSelectedItem();
        String strJam = (String) LembuLamaCBox.getSelectedItem();

        if (nama == null || strJam == null) {
            JOptionPane.showMessageDialog(this, "Pilih karyawan dan lama lembur!");
            return;
        }

        // Input tanggal melalui dialog
        String tanggal = JOptionPane.showInputDialog(this, "Masukkan tanggal lembur (format: yyyy-MM-dd):", "Tanggal Lembur", JOptionPane.QUESTION_MESSAGE);
        if (tanggal == null || tanggal.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tanggal harus diisi!");
            return;
        }

        int jam;
        try {
            jam = Integer.parseInt(strJam.replace(" jam", ""));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jam lembur tidak valid!");
            return;
        }

        Pegawai p = KaryawanController.daftarKaryawan().stream()
                .filter(k -> k.getNama().equals(nama)).findFirst().orElse(null);
        if (p != null) {
            LemburController.tambahLembur(p.getId(), tanggal, jam);
            JOptionPane.showMessageDialog(this, "Lembur berhasil disimpan.");
            new ManageAbsensi().setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Karyawan tidak ditemukan!");
        }
    }//GEN-LAST:event_SimpanLembuBTActionPerformed

    private void LembuNamaCBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LembuNamaCBoxActionPerformed
        String nama = (String) LembuNamaCBox.getSelectedItem();
        if (nama != null) {
            Pegawai p = KaryawanController.daftarKaryawan().stream()
                    .filter(k -> k.getNama().equals(nama))
                    .findFirst().orElse(null);
            if (p != null) {
                LembuDeptCBox2.setSelectedItem(p.getDepartemen().getNama());
            }
        }
    }//GEN-LAST:event_LembuNamaCBoxActionPerformed

