    private void CariBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CariBTActionPerformed
        String nama = TFNamaKwn.getText().trim();
        String strUsia = TFUsiaKwn.getText().trim();
        String jenisKelamin = (String) KelaminCBox.getSelectedItem();
        String deptNama = (String) TambahDeptCBox.getSelectedItem();
        String posisiNama = (String) TambahPosisiCBox.getSelectedItem();
        String tipe = (String) TipeCBox.getSelectedItem();

        // Validasi sederhana
        if (nama.isEmpty() || strUsia.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan Usia harus diisi!");
            return;
        }
        int usia;
        try {
            usia = Integer.parseInt(strUsia);
            if (usia <= 0) {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Usia harus angka positif!");
            return;
        }

        // Cari objek Departemen dan Jabatan berdasarkan nama
        Departemen dept = KaryawanController.daftarDepartemen().stream()
                .filter(d -> d.getNama().equals(deptNama)).findFirst().orElse(null);
        Jabatan jab = KaryawanController.daftarJabatan().stream()
                .filter(j -> j.getNama().equals(posisiNama)).findFirst().orElse(null);

        if (dept == null || jab == null) {
            JOptionPane.showMessageDialog(this, "Data departemen atau posisi tidak valid!");
            return;
        }

        Pegawai pegawai;
        if (tipe.equals("Tetap")) {
            KaryawanTetap kt = new KaryawanTetap();
            // Minta input gaji pokok dan tunjangan via dialog
            String gajiStr = JOptionPane.showInputDialog(this, "Masukkan Gaji Pokok:", "5000000");
            String tunjanganStr = JOptionPane.showInputDialog(this, "Masukkan Tunjangan:", "500000");
            double gajiPokok, tunjangan;
            try {
                gajiPokok = Double.parseDouble(gajiStr);
                tunjangan = Double.parseDouble(tunjanganStr);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Gaji dan tunjangan harus angka!");
                return;
            }
            kt.setGajiPokok(gajiPokok);
            kt.setTunjangan(tunjangan);
            pegawai = kt;
        } else { // Magang
            Magang m = new Magang();
            String uangHarianStr = JOptionPane.showInputDialog(this, "Masukkan Uang Harian:", "100000");
            String hariKerjaStr = JOptionPane.showInputDialog(this, "Masukkan Jumlah Hari Kerja per Bulan:", "20");
            double uangHarian;
            int hariKerja;
            try {
                uangHarian = Double.parseDouble(uangHarianStr);
                hariKerja = Integer.parseInt(hariKerjaStr);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Uang harian dan hari kerja harus angka!");
                return;
            }
            m.setUangHarian(uangHarian);
            m.setHariKerja(hariKerja);
            pegawai = m;
        }

        pegawai.setNama(nama);
        pegawai.setUsia(usia);
        pegawai.setJenisKelamin(jenisKelamin);
        pegawai.setDepartemen(dept);
        pegawai.setJabatan(jab);
        pegawai.setTipe(tipe);
        pegawai.setStatusAktif(true);
        pegawai.setKuotaCuti(12);

        // Simpan ke database
        KaryawanController.tambahKaryawan(pegawai);
        JOptionPane.showMessageDialog(this, "Karyawan berhasil ditambahkan!");
        new ManageKaryawan().setVisible(true);
        dispose();
    
    }//GEN-LAST:event_CariBTActionPerformed

