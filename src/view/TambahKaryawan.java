    private void isiCombo() {
        // Jenis Kelamin
        KelaminCBox.setModel(new DefaultComboBoxModel<>(new String[]{"L", "P"}));

        // Tipe Karyawan
        TipeCBox.setModel(new DefaultComboBoxModel<>(new String[]{"Tetap", "Magang"}));

        // Departemen
        List<Departemen> deptList = KaryawanController.daftarDepartemen();
        TambahDeptCBox.removeAllItems();
        for (Departemen d : deptList) {
            TambahDeptCBox.addItem(d.getNama());
        }

        // Posisi
        List<Jabatan> jabList = KaryawanController.daftarJabatan();
        TambahPosisiCBox.removeAllItems();
        for (Jabatan j : jabList) {
            TambahPosisiCBox.addItem(j.getNama());
        }
    }

