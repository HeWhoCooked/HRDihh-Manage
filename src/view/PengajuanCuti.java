    private void buatTabelCuti() {
        // Hapus semua komponen lama di pnlTempatTabel
        pnlTempatTabel.removeAll();
        pnlTempatTabel.setLayout(new BorderLayout());

        // Model tabel
        modelTabel = new DefaultTableModel(
                new String[]{"ID", "Nama Karyawan", "Tanggal Mulai", "Tanggal Selesai", "Status", "Aksi"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int col) {
                return col == 5; // kolom aksi bisa diedit
            }
        };
        tblCuti = new JTable(modelTabel);
        // Sembunyikan kolom ID
        tblCuti.getColumnModel().getColumn(0).setMinWidth(0);
        tblCuti.getColumnModel().getColumn(0).setMaxWidth(0);
        tblCuti.getColumnModel().getColumn(0).setWidth(0);
        // Lebar kolom aksi
        tblCuti.getColumnModel().getColumn(5).setMinWidth(150);
        tblCuti.getColumnModel().getColumn(5).setPreferredWidth(150);
        tblCuti.setRowHeight(40);

        // Styling header
        JTableHeader header = tblCuti.getTableHeader();
        header.setDefaultRenderer(new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                setBackground(Color.BLACK);
                setForeground(Color.WHITE);
                setFont(new Font("Roboto", Font.BOLD, 14));
                setHorizontalAlignment(SwingConstants.CENTER);
                return this;
            }
        });
        header.setPreferredSize(new Dimension(header.getPreferredSize().width, 40));

        // Tombol aksi
        tblCuti.getColumnModel().getColumn(5).setCellRenderer(new TombolRenderer());
        tblCuti.getColumnModel().getColumn(5).setCellEditor(new TombolEditor());

        // Panel bawah untuk tombol tambah cuti
        JPanel panelBawah = new JPanel(new FlowLayout(FlowLayout.RIGHT));
        btnTambahCuti = new JButton("+ Ajukan Cuti");
        btnTambahCuti.setFont(new Font("Roboto", Font.BOLD, 18));
        btnTambahCuti.setBackground(Color.BLACK);
        btnTambahCuti.setForeground(Color.WHITE);
        btnTambahCuti.addActionListener(e -> bukaFormAjuan());
        panelBawah.add(btnTambahCuti);

        pnlTempatTabel.add(new JScrollPane(tblCuti), BorderLayout.CENTER);
        pnlTempatTabel.add(panelBawah, BorderLayout.SOUTH);

        pnlTempatTabel.revalidate();
        pnlTempatTabel.repaint();
    }

    private void muatDataCuti() {
        modelTabel.setRowCount(0);
        List<Cuti> list = CutiController.semuaCuti();
        for (Cuti c : list) {
            if (c.getStatus().equals("Pending")) {
                modelTabel.addRow(new Object[]{
                    c.getId(),
                    c.getKaryawanNama(),
                    c.getTanggalMulai(),
                    c.getTanggalSelesai(),
                    c.getStatus(),
                    "Aksi"
                });
            }
        }
    }

    private void bukaFormAjuan() {
        JComboBox<String> cmbKaryawan = new JComboBox<>();
        for (Pegawai p : KaryawanController.daftarKaryawan()) {
            cmbKaryawan.addItem(p.getNama());
        }
        JTextField txtMulai = new JTextField(10);
        JTextField txtSelesai = new JTextField(10);
        JTextArea txtAlasan = new JTextArea(3, 20);
        JPanel panel = new JPanel(new GridLayout(0, 2, 10, 10));
        panel.add(new JLabel("Karyawan:"));
        panel.add(cmbKaryawan);
        panel.add(new JLabel("Tanggal Mulai (yyyy-MM-dd):"));
        panel.add(txtMulai);
        panel.add(new JLabel("Tanggal Selesai:"));
        panel.add(txtSelesai);
        panel.add(new JLabel("Alasan:"));
        panel.add(new JScrollPane(txtAlasan));

        int result = JOptionPane.showConfirmDialog(this, panel, "Ajukan Cuti", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        if (result == JOptionPane.OK_OPTION) {
            String nama = (String) cmbKaryawan.getSelectedItem();
            Pegawai p = KaryawanController.daftarKaryawan().stream()
                    .filter(k -> k.getNama().equals(nama)).findFirst().orElse(null);
            if (p != null) {
                CutiController.ajukanCuti(p.getId(), txtMulai.getText(), txtSelesai.getText(), txtAlasan.getText());
                muatDataCuti();
                JOptionPane.showMessageDialog(this, "Pengajuan cuti berhasil.");
            } else {
                JOptionPane.showMessageDialog(this, "Karyawan tidak ditemukan!");
            }
        }
    }

