    // Inner class untuk editor tombol
    class TombolEditor extends AbstractCellEditor implements TableCellEditor {

        JPanel panel = new JPanel(new FlowLayout(FlowLayout.CENTER, 5, 5));
        JButton btnSetuju = new JButton("Setuju");
        JButton btnTolak = new JButton("Tolak");
        int row;

        public TombolEditor() {
            panel.setBackground(Color.WHITE);
            btnSetuju.setBackground(new Color(46, 204, 113));
            btnSetuju.setForeground(Color.WHITE);
            btnTolak.setBackground(new Color(231, 76, 60));
            btnTolak.setForeground(Color.WHITE);
            panel.add(btnSetuju);
            panel.add(btnTolak);
            btnSetuju.addActionListener(e -> {
                fireEditingStopped();
                int id = (int) modelTabel.getValueAt(row, 0);
                try {
                    CutiController.setujuiCuti(id);
                    muatDataCuti();
                    JOptionPane.showMessageDialog(tblCuti, "Cuti disetujui. Kuota cuti telah dikurangi.");
                } catch (IllegalStateException ex) {
                    JOptionPane.showMessageDialog(tblCuti, ex.getMessage(), "Gagal", JOptionPane.ERROR_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(tblCuti, "Terjadi kesalahan: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            });
            btnTolak.addActionListener(e -> {
                fireEditingStopped();
                int id = (int) modelTabel.getValueAt(row, 0);
                CutiController.tolakCuti(id);
                muatDataCuti();
                JOptionPane.showMessageDialog(tblCuti, "Cuti ditolak dan dihapus.");
            });
        }

        @Override
        public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int col) {
            this.row = row;
            return panel;
        }

        @Override
        public Object getCellEditorValue() {
            return null;
        }
    }


